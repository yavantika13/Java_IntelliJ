public class Switch
{
    public static void main(String[] args) {
        int a=5;
        switch (a) {
            case 1:
            case 2:
            case 3:
            case 4:
                System.out.println("hello");
                break;
            case 5:
                System.out.println("hiii");
                break;
            default:
                System.out.println("byee");

        }
    }
}
